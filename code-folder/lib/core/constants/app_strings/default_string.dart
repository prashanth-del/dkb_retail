class DefaultString {
  static I18nSource? _src;
  static void bind(I18nSource source) => _src = source;

  const DefaultString._internal();
  static const instance = DefaultString._internal();

  // shared helpers usable from part-files
  String _i18nText({required String key, required String fallback}) =>
      (_src?.text(key, fallback: fallback)) ?? fallback;

  String _i18nValidator({required String key, required String fallback}) =>
      (_src?.validation(key, fallback: fallback)) ?? fallback;
}
